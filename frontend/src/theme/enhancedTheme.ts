// 创建主题函数
export const createEnhancedTheme = (mode: 'light' | 'dark' = 'light'): Theme => {
  const palette = mode === 'light' ? colorPalette : darkColorPalette;

  const themeOptions: ThemeOptions = {
    palette: {
      mode,
      ...palette,
    },
    typography,
    breakpoints,
    shadows: shadows as any,
    transitions: animations.transitions,
    components: getComponentOverrides(palette),
    spacing: 8,
    shape: {
      borderRadius: 8,
    },
    zIndex: {
      mobileStepper: 1000,
      fab: 1050,
      speedDial: 1050,
      appBar: 1100,
      drawer: 1200,
      modal: 1300,
      snackbar: 1400,
      tooltip: 1500,
    },
  };

  return createTheme(themeOptions);
};

// 预定义主题
export const lightTheme = createEnhancedTheme('light');
export const darkTheme = createEnhancedTheme('dark');

// 主题工具函数
export const getThemeMode = (): 'light' | 'dark' => {
  if (typeof window !== 'undefined') {
    const saved = localStorage.getItem('themeMode');
    if (saved === 'light' || saved === 'dark') {
      return saved;
    }
    return window.matchMedia('(prefers-color-scheme: dark)').matches ? 'dark' : 'light';
  }
  return 'light';
};

export const setThemeMode = (mode: 'light' | 'dark') => {
  if (typeof window !== 'undefined') {
    localStorage.setItem('themeMode', mode);
  }
};

export default {
  createEnhancedTheme,
  lightTheme,
  darkTheme,
  getThemeMode,
  setThemeMode,
}; 